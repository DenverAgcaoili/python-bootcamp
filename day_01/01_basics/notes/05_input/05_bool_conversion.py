# Numbers to bool
print(bool(0))  # False
print(bool(42))  # True
print(bool(-1))  # True

# Strings to bool
print(bool(""))  # False (empty string)
print(bool("hi"))  # True
print(bool("0"))  # True

# Lists to bool
print(bool([]))  # False
print(bool([1, 2]))  # True

# None to bool
print(bool(None))  # False
